package daliyquiz240129;

public class DailyQuiz02_03 {
    public static void main(String[] args) {
        // 아래 코드의 출력값은 무엇일까요?
        int num1 = 5;
        int num2 = 7;

        System.out.println((num1 > 5) && (num2 > 5));
        System.out.println((num1 > 5) || (num2 > 5));
        System.out.println(!((num1 > 5) && (num2 > 5)));
    }
}
