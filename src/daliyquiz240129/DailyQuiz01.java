package daliyquiz240129;

public class DailyQuiz01 {

    public static void main(String[] args) {
        /*
        * 아래 2개의 변수 number1, number2의
        * 사칙 연산 결과(result1 ~ result4)를 정수로 출력하세요.
        * (사칙연산: +, -, *, /) 주석처리 된 곳에 코드를 채우면 됩니다
        */
        // 사칙연산 코드 추가
        int number1 = 10;
        double number2 = 2.0;

        double result1 = number1+number2;
        double result2 = number1-number2;
        double result3 = number1*number2;
        double result4 = number1/number2;

        System.out.println(result1);
        System.out.println(result2);
        System.out.println(result3);
        System.out.println(result4);
    }

}
